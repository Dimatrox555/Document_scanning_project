# Однозначные замены: латиница → кириллица (без учёта контекста)
# Порядок не важен
CHAR_MAP: dict[str, str] = {
    "A": "А", "B": "В", "E": "Е", "K": "К", "M": "М",
    "H": "Н", "O": "О", "P": "Р", "C": "С", "T": "Т",
    "Y": "У", "X": "Х",
    "a": "а", "e": "е", "o": "о",
    "p": "р", "c": "с", "y": "у", "x": "х",
}

# Контекстные правила: (паттерн, замена, описание)
# Применяются ПОСЛЕ CHAR_MAP — работают на уже нормализованном тексте
# Порядок важен
_Д = r"[А-ЯЁа-яё]"  # любая кириллическая буква

CONTEXT_RULES: list[tuple[str, str, str]] = [
    # цифра/символ рядом с цифрами → цифра
    (r"(?<=[\d\-])[О]|[О](?=[\d\-])",          "0", "О рядом с цифрой → 0"),
    (r"(?<=\d)[б]|[б](?=\d)",                   "6", "б рядом с цифрой → 6"),
    (r"(?<=\d)[ЗЗ]|[З](?=\d)",                  "3", "З рядом с цифрой → 3"),
    (r"(?<=\d)[з]|[з](?=\d)",                   "3", "з рядом с цифрой → 3"),
    (r"(?<=\d)[lI]|[lI](?=\d)",                 "1", "l/I рядом с цифрой → 1"),
    (r"(?<=\d)[ч]|[ч](?=\d)",                   "4", "ч рядом с цифрой → 4"),

    # цифра рядом с буквами → буква
    (rf"(?<={_Д})[0]|[0](?={_Д})",              "О", "0 рядом с буквой → О"),
    (rf"(?<={_Д})[6]|[6](?={_Д})",              "б", "6 рядом с буквой → б"),
    (rf"(?<={_Д})[3]|[3](?={_Д})",              "З", "3 рядом с буквой → З"),
    (rf"(?<={_Д})[4]|[4](?={_Д})",              "ч", "4 рядом с буквой → ч"),
]
